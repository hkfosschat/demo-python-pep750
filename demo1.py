#!/usr/bin/env python3

# Classic String Operations Examples

name = "Panda"

# 使用 f-string
print(f"Hello {name}")

# 使用 String Formatting Operator "%"
# 詳情：https://python-reference.readthedocs.io/en/latest/docs/str/formatting.html
print("Hello %" % name)

# 使用 str.format()
# 詳情：https://docs.python.org/3/library/stdtypes.html#str.format
print("Hello {}".format(name))



